orders = []

for prod_id, total_qty in qty_data.items():

    if total_qty == 0:
        continue

    price = price_data.get(prod_id)
    if price is None:
        continue

    remaining_qty = total_qty

    while remaining_qty > 0:

        # случайная дата в пределах года
        order_date = random.choice(date_range)

        # равномерные партии (до 5% остатка)
        max_chunk = max(1, int(total_qty * 0.05))
        qty_order = random.randint(1, max_chunk)
        qty_order = min(qty_order, remaining_qty)

        amount = qty_order * price

        orders.append({
            "product_category": 2,
            "order_date": order_date,
            "amount": round(amount, 2),
            "qty": qty_order,
            "prod_id": prod_id
        })

        remaining_qty -= qty_order

df_orders = pd.DataFrame(orders)
df_orders = df_orders.sort_values("order_date").reset_index(drop=True)

